import {v4 as uuid} from 'uuid';

const NUM_UUIDS = 1000;
const uuids = new Array(NUM_UUIDS);

for (let i = 0; i < 1000; i++) {
  uuids[i] = uuid();
}

console.log(`// DO NOT EDIT -- Autogenerated by genStringConstants.ts

export const UUIDS = ${JSON.stringify(uuids, null, 2)};

export const NUM_UUIDS = ${NUM_UUIDS};
`);
